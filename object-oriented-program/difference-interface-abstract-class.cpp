/*
    Difference between Interface vs Abstreact class

// Interface

1. Interface support multiple inheritance.
2. Interface does not contain Data Member
3. Interface does not contain Constructors
4. An interface Contains only incomplete member
    (signature of member)
5. An interface cannot have access modifiers by
    default everything is assumed as public
6. Member of interface can not be Static


// Abstract class

1. Abstract class does not support multiple inheritance.
2. Abstract class contains Data Member
3. Abstract class contains Constructors
4 .An abstract class Contains both Incomplete
    (abstract) and complete member
5. An abstract class can contain access modifiers
    for the subs, functions, properties
6 .Only Complete Member of abstract class can be Static

*/